    private void BtnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEditarActionPerformed
        if (TblProductos.getSelectedRowCount() == 1) {
            try {
                this.Id_producto = Integer.parseInt(String.valueOf(TblProductos.getValueAt(TblProductos.getSelectedRow(), 0)));
                int Id_Categoria = Integer.parseInt(String.valueOf(TblProductos.getValueAt(TblProductos.getSelectedRow(), 1)));
                String categoriaNombre = String.valueOf(TblProductos.getValueAt(TblProductos.getSelectedRow(), 2));
                int Id_Marca = Integer.parseInt(String.valueOf(TblProductos.getValueAt(TblProductos.getSelectedRow(), 3)));
                String marcaNombre = String.valueOf(TblProductos.getValueAt(TblProductos.getSelectedRow(), 4));
                String Nombre = String.valueOf(TblProductos.getValueAt(TblProductos.getSelectedRow(), 5));
                String Descripcion = String.valueOf(TblProductos.getValueAt(TblProductos.getSelectedRow(), 6));
                double Precio_U = Double.parseDouble(String.valueOf(TblProductos.getValueAt(TblProductos.getSelectedRow(), 7)));
                int Stock = Integer.parseInt(String.valueOf(TblProductos.getValueAt(TblProductos.getSelectedRow(), 8)));

                this.producto = new Producto(this.Id_producto, Id_Categoria, categoriaNombre, Id_Marca, marcaNombre, Nombre, Descripcion, Precio_U, Stock);

                this.TxtNombre.setText(Nombre);
                this.TxtDescripcion.setText(Descripcion);
                this.TxtStock.setText(String.valueOf(Stock));
                this.TxtPreciou.setText(String.valueOf(Precio_U));

                for (int i = 0; i < CbxCategoria.getItemCount(); i++) {
                    Categoria cat = (Categoria) CbxCategoria.getItemAt(i);
                    if (cat.getId_Categoria() == Id_Categoria) {
                        CbxCategoria.setSelectedItem(cat);
                        break;
                    }
                }

                for (int i = 0; i < CbxMarca.getItemCount(); i++) {
                    Marca mar = (Marca) CbxMarca.getItemAt(i);
                    if (mar.getId_Marca() == Id_Marca) {
                        CbxMarca.setSelectedItem(mar);
                        break;
                    }
                }

                TabProductos.setEnabledAt(0, false);
                TabProductos.setEnabledAt(1, true);
                TabProductos.setSelectedIndex(1);
                this.accion = "Editar";
                BtnGuardar.setText("Editar");

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Error al parsear datos de la tabla: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error al cargar datos para edición: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un producto para editar.");
        }
    }//GEN-LAST:event_BtnEditarActionPerformed

    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        if (TblProductos.getSelectedRowCount() == 1) {
            String Id_Producto = String.valueOf(TblProductos.getValueAt(TblProductos.getSelectedRow(), 0));
            int confirmacion = JOptionPane.showConfirmDialog(null,
                    "¿Estás seguro de eliminar este Producto?",
                    "Confirmar",
                    JOptionPane.YES_NO_OPTION);
            if (confirmacion == JOptionPane.YES_OPTION) {
                int id = Integer.parseInt(Id_Producto);
                String resp = this.CONTROL.eliminar(id);
                if (resp.equals("Ok")) {
                    JOptionPane.showMessageDialog(null, "Producto eliminado con éxito");
                    this.listar("");
                } else {
                    JOptionPane.showMessageDialog(null, resp);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un producto a eliminar");
        }
    }//GEN-LAST:event_BtnEliminarActionPerformed

