    private void BtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGuardarActionPerformed
        producto = new Entidades.Producto();

        Categoria categoriaSeleccionada = (Categoria) this.CbxCategoria.getSelectedItem();
        if (categoriaSeleccionada != null) {
            producto.setId_Categoria(categoriaSeleccionada.getId_Categoria());
            producto.setcategoriaNombre(categoriaSeleccionada.getNombre());
        }

        Marca marcaSeleccionada = (Marca) this.CbxMarca.getSelectedItem();
        if (marcaSeleccionada != null) {
            producto.setId_Marca(marcaSeleccionada.getId_Marca());
            producto.setmarcaNombre(marcaSeleccionada.getNombre());
        }
        producto.setNombre(this.TxtNombre.getText());
        producto.setDescripcion(this.TxtDescripcion.getText());
        producto.setStock(Integer.parseInt(this.TxtStock.getText()));
        producto.setPrecio_U(Double.parseDouble(this.TxtPreciou.getText()));

        if (this.accion.equals("Guardar")) {
            resp = this.CONTROL.insertar(producto);
            if (resp.equals("Ok")) {
                JOptionPane.showMessageDialog(null, "Producto registrado con exito");
                this.limpiar();
                this.listar("");
                TabProductos.setEnabledAt(0, true);
                TabProductos.setEnabledAt(1, false);
                TabProductos.setSelectedIndex(0);
            } else {
                JOptionPane.showMessageDialog(null, resp);
            }
        } else if (this.accion.equals("Editar")) {
            producto.setId_Producto(this.Id_producto);
            resp = this.CONTROL.modificar(producto);
            if (resp.equals("Ok")) {
                JOptionPane.showMessageDialog(null, "Producto modificado con exito");
                this.limpiar();
                this.listar("");
                TabProductos.setEnabledAt(0, true);
                TabProductos.setEnabledAt(1, false);
                TabProductos.setSelectedIndex(0);
            } else {
                JOptionPane.showMessageDialog(null, resp);
            }
        }
    }//GEN-LAST:event_BtnGuardarActionPerformed

