    private void BtnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEditarActionPerformed
        if (TblEmpleados.getSelectedRowCount() == 1) {
            try {
                this.Id_empleado = Integer.parseInt(String.valueOf(TblEmpleados.getValueAt(TblEmpleados.getSelectedRow(), 0)));
                String Nombres = String.valueOf(TblEmpleados.getValueAt(TblEmpleados.getSelectedRow(), 1));
                String DNI = String.valueOf(TblEmpleados.getValueAt(TblEmpleados.getSelectedRow(), 2));
                String Telefono = String.valueOf(TblEmpleados.getValueAt(TblEmpleados.getSelectedRow(), 3));
                String Turno = String.valueOf(TblEmpleados.getValueAt(TblEmpleados.getSelectedRow(), 4));
                double Salario = Double.parseDouble(String.valueOf(TblEmpleados.getValueAt(TblEmpleados.getSelectedRow(), 5)));

                this.empleado = new Empleado(this.Id_empleado, Nombres, DNI, Telefono, Turno, Salario);

                this.TxtNombre.setText(Nombres);
                this.TxtDoc.setText(DNI);
                this.TxtTelefono.setText(String.valueOf(Telefono));
                this.TxtSalario.setText(String.valueOf(Salario));
                CbxTurno.setSelectedItem(String.valueOf(Turno));
               


                TabEmpleados.setEnabledAt(0, false);
                TabEmpleados.setEnabledAt(1, true);
                TabEmpleados.setSelectedIndex(1);
                this.accion = "Editar";
                BtnGuardar.setText("Editar");

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Error al parsear datos de la tabla: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error al cargar datos para edición: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un empleado para editar.");
        }
    }//GEN-LAST:event_BtnEditarActionPerformed

