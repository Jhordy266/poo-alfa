    private void BtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGuardarActionPerformed
        Empleado emp = new Empleado();
        emp.setNombres(TxtNombre.getText());
        emp.setDNI(TxtDoc.getText());
        emp.setTelefono(TxtTelefono.getText());
        emp.setTurno((String) CbxTurno.getSelectedItem());
        emp.setSalario(Double.parseDouble(this.TxtSalario.getText()));

        if (this.accion.equals("Guardar")) {
            resp = this.CONTROL.insertar(emp);
            if (resp.equals("Ok")) {
                JOptionPane.showMessageDialog(null, "Categoria registrado con exito");
                this.limpiar();
                this.listar("");
                TabEmpleados.setEnabledAt(0, true);
                TabEmpleados.setEnabledAt(1, false);
                TabEmpleados.setSelectedIndex(0);
            } else {
                JOptionPane.showMessageDialog(null, resp);
            }
        } else if (this.accion.equals("Editar")) {
            emp.setId_Empleado(this.Id_empleado);
            resp = this.CONTROL.modificar(emp);
            if (resp.equals("Ok")) {
                JOptionPane.showMessageDialog(null, "Categoria modificada con exito");
                this.limpiar();
                this.listar("");
                TabEmpleados.setEnabledAt(0, true);
                TabEmpleados.setEnabledAt(1, false);
                TabEmpleados.setSelectedIndex(0);
            } else {
                JOptionPane.showMessageDialog(null, resp);
            }
        }
    }//GEN-LAST:event_BtnGuardarActionPerformed

    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        if (TblEmpleados.getSelectedRowCount() == 1) {
            String Id_Empleado = String.valueOf(TblEmpleados.getValueAt(TblEmpleados.getSelectedRow(), 0));
            int confirmacion = JOptionPane.showConfirmDialog(null,
                    "¿Estás seguro de eliminar este Producto?",
                    "Confirmar",
                    JOptionPane.YES_NO_OPTION);
            if (confirmacion == JOptionPane.YES_OPTION) {
                int id = Integer.parseInt(Id_Empleado);
                String resp = this.CONTROL.eliminar(id);
                if (resp.equals("Ok")) {
                    JOptionPane.showMessageDialog(null, "Empleado eliminado con éxito");
                    this.listar("");
                } else {
                    JOptionPane.showMessageDialog(null, resp);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un producto a eliminar");
        }
    }//GEN-LAST:event_BtnEliminarActionPerformed

